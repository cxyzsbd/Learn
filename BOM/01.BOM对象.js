/* 
JS : BOM  DOM  ECMA 标准
DOM： document object model,文档对象模型
BOM : browser object model,浏览器对象模型 和浏览器、窗口有关的内容
BOM 顶级对象 window

BOM对象：
        - Window —— 代表浏览器窗口（全局对象）
        - Navigator —— 浏览器的对象（可以用来识别浏览器）
        - Location —— 浏览器的地址栏信息
        - History —— 浏览器的历史记录（控制浏览器前进后退）
        - Screen —— 屏幕的信息

BOM对象都是作为window对象的属性保存的,所以可以直接在JS中访问这些对象



 */